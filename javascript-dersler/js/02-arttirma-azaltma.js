counter=counter+1 //sayıyı 1 arttırdık (uzun yol) 321
counter +=1 //sayıyı 1 arttırdık 322
counter ++ //sayıyı 1 arttırdık en çok kullanılacak yöntem 323
console.log(counter)

counter -- // sayıyı 1 azalttık 322
counter -= 1 //sayıyı 1 azalttık 321
console.log(counter)

counter *=10 //sayıyı 10 ile çarptık 3210 
counter /=2 //sayıyı 2 ye böldük 1605
console.log(counter)

